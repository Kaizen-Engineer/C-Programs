#include<stdio.h>

int main() {
	int a = 50;
	int b = 100;

	printf("Hello, Kaizen Engineering!! \n");

	int sum = a + b;
	printf("Sum of a and b is %d\n", sum);

	int sub = a - b;
	printf("Sub of a and b is %d\n", sub);

	int prod = a * b;
	printf("Product of a and b is %d\n", prod);

	int div = a / b;
	printf("Divison of a and b is %d\n", div);

	return 0;
}